// Find FAQ by keywords
export function findFAQByKeywords(text: string): FAQItem | null {
  const lowerText = text.toLowerCase()

  for (const item of FAQ_DATA) {
    for (const keyword of item.keywords) {
      if (lowerText.includes(keyword.toLowerCase())) {
        return item
      }
    }
  }

  return null
}

// Get FAQ by category
export function getFAQByCategory(categoryId: string): FAQItem[] {
  return FAQ_DATA.filter(item => item.category === categoryId)
}

// Get FAQ by ID
export function getFAQById(id: string): FAQItem | undefined {
  return FAQ_DATA.find(item => item.id === id)
}
